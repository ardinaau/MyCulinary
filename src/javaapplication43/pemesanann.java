    //action button pesan yang digunakan untuk memasukkan data ke dalam database dengan query insert
    private void pesanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanBtnActionPerformed
        // TODO add your handling code here:
        //memecah data dari combo box karena di dalam combo box ada id & nama makanan
        String makananItem = comboBoxMakanan.getSelectedItem().toString();
        String makanan = makananItem.split("\\)", 2)[0];

        //bagian masukkan data ke dalam database ada di action BUTTON DAFTAR
        try {
            Connection con = MySqlConnection.getConnection();
            //query insert data ke dalam database mysql
            pst = con.prepareStatement("INSERT INTO pemesanan (id_makanan, harga, jumlah, total, no_meja) VALUES(?,?,?,?,?)");
            //validasi ketika textField kosong

            if (jumlahTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Jumlah tidak boleh kosong");
            } else if (totalTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Total tidak boleh kosong");
            } else {
                //digunakan untuk memasukkan data ke masing2 variabel textfield seperti namaTxt, dll

                //misal bingung pst sama rs bisa diliat di variabel diatas
                pst.setString(1, makanan);
                pst.setString(2, hargaTxt.getText());
                pst.setString(3, jumlahTxt.getText());
                pst.setString(4, totalTxt.getText());
                pst.setString(5, noMejaTxt.getText());
                pst.executeUpdate();

                //setelah nginput data kasih ini biar textfieldnya kosong lagi
                hargaTxt.setText("");
                totalTxt.setText("");
                jumlahTxt.setText("");
                noMejaTxt.setText("");
                comboBoxMakanan.setSelectedItem("");

                //setelah daftar muncul pop up dafatar berhasil dan akan tampil frame baru
                JOptionPane.showMessageDialog(null, "Pesanan sedang diproses, Mohon tunggu di meja anda");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.getMessage());

        }
    }//GEN-LAST:event_pesanBtnActionPerformed

    
