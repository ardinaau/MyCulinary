    //method ini berguna untuk mengambil data dan dimasukkan ke dalam combo box.
    private void updateCombo() {
        String sql = "Select * from makanan";
        try {
            Connection con = MySqlConnection.getConnection();
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                comboBoxMakanan.addItem(rs.getString("id_makanan") + ") " + rs.getString("nama"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    } 
    
    //mengambil data harga ketika memilih combo box, harga dari makanan akan keluar di textfield harga
    private void getHarga() {
        String makananItem = comboBoxMakanan.getSelectedItem().toString();
        String makanan = makananItem.split("\\)", 2)[0];//memecah string

        String sql = "SELECT * from makanan where id_makanan =  " + makanan;
        try {
            Connection con = MySqlConnection.getConnection();
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                hargaTxt.setText(rs.getString("harga"));//meng set teks ke harga dan mengambil data harga
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }

    //method untuk menghitung total harga dikali jumlah makanan
    private void getTotal() {
        try {
            int harga = Integer.parseInt(hargaTxt.getText());
            int jumlah = Integer.parseInt(jumlahTxt.getText());
            double total = (harga * jumlah);
            totalTxt.setText(String.valueOf(total));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

    }

